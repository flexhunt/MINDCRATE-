-- Create custom_emojis table
CREATE TABLE IF NOT EXISTS custom_emojis (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    original_emoji TEXT NOT NULL UNIQUE,
    custom_url TEXT NOT NULL,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Create index for faster lookups
CREATE INDEX IF NOT EXISTS idx_custom_emojis_original ON custom_emojis(original_emoji);
CREATE INDEX IF NOT EXISTS idx_custom_emojis_active ON custom_emojis(is_active);

-- Enable RLS
ALTER TABLE custom_emojis ENABLE ROW LEVEL SECURITY;

-- Allow public read access for active emojis
CREATE POLICY "Allow public read access to active emojis" ON custom_emojis
    FOR SELECT USING (is_active = true);

-- Allow admin full access
CREATE POLICY "Allow admin full access to custom_emojis" ON custom_emojis
    FOR ALL USING (
        EXISTS (
            SELECT 1 FROM profiles 
            WHERE profiles.id = auth.uid() 
            AND profiles.is_admin = true
        )
    );

